# -*- coding: utf-8 -*-
"""
01 弹跳球 —— YADE DEM 入门示例
========================================
场景：一个球体从高处自由下落，撞击固定方块（地面）后反弹，
经过多次弹跳因阻尼作用最终静止。

运行方式：
    yadedaily bouncing_sphere.py
"""

# ============================================================
# 第一部分：定义材料 (Material)
# ============================================================
# FrictMat 是最基础的摩擦材料模型
#   young         ：杨氏模量 [Pa]，控制接触刚度（越大越硬）
#   poisson       ：泊松比，影响法向/切向刚度比（通常 0.2~0.4）
#   frictionAngle ：颗粒间摩擦角 [rad]（0.5 rad ≈ 28.6°）
#   density       ：密度 [kg/m³]，影响颗粒质量和惯性
O.materials.append(FrictMat(
    young=5e6,          # 5 MPa — 较软的材料，便于观察变形和弹跳
    poisson=0.3,
    frictionAngle=0.5,
    density=2600        # 类似砂土密度
))

# ============================================================
# 第二部分：定义引擎管线 (Engine Pipeline)
# ============================================================
# O.engines 定义了每个时间步中依次执行的操作序列。
# 执行顺序：ForceResetter → InsertionSortCollider → InteractionLoop → NewtonIntegrator

O.engines = [
    # ------ 步骤 1：力的清零 ------
    # 每个时间步开始时，将所有物体上累积的力和力矩归零。
    # 这样每步的接触力都是从零开始计算，而不是累加到上一步的力上。
    ForceResetter(),

    # ------ 步骤 2：碰撞检测（粗筛阶段） ------
    # 检测哪些物体的 AABB（轴对齐包围盒）发生了重叠，产生候选接触对。
    # Bo1_Sphere_Aabb()：为 Sphere 形状的物体生成/更新 AABB
    # Bo1_Box_Aabb()：为 Box 形状的物体生成/更新 AABB
    InsertionSortCollider([Bo1_Sphere_Aabb(), Bo1_Box_Aabb()]),

    # ------ 步骤 3：接触力计算 ------
    # 对上一步检测出的所有候选接触对，执行三个阶段的操作：
    InteractionLoop(
        # Ig2（接触几何）：判断是否真的接触，计算接触点、重叠量、法向量等
        [Ig2_Sphere_Sphere_ScGeom(),   # 球体-球体接触几何
         Ig2_Box_Sphere_ScGeom()],     # 盒子-球体接触几何
        # Ip2（接触物理）：根据两个物体的材料参数，计算接触的力学参数
        [Ip2_FrictMat_FrictMat_FrictPhys()],  # 两个 FrictMat → FrictPhys
        # Law2（本构律）：根据几何量和力学参数，计算接触力和力矩
        [Law2_ScGeom_FrictPhys_CundallStrack()]  # Cundall-Strack 弹簧-滑块模型
    ),

    # ------ 步骤 4：运动积分（牛顿第二定律） ------
    # 根据合力和合力矩，更新每个物体的加速度、速度和位置。
    # damping=0.05：5% 局部非粘性阻尼，模拟能量耗散（值越小弹跳越明显）
    # gravity=(0, 0, -9.81)：z 轴向下的重力加速度 9.81 m/s²
    NewtonIntegrator(damping=0.05, gravity=(0, 0, -9.81)),
]

# ============================================================
# 第二部分：创建仿真物体 (Bodies)
# ============================================================

# ------ 创建地面（固定方块） ------
# box() 创建一个长方体形状的 Body
#   center  = [0, 0, 0]  ：方块中心位于坐标原点
#   extents = [.5, .5, .5]：半尺寸，实际尺寸为 1×1×1
#   fixed   = True        ：固定不动（质量设为无穷大，不受力影响）
#   color   = [1, 0, 0]   ：红色（RGB 值，范围 0~1）
O.bodies.append(box(center=[0, 0, 0], extents=[.5, .5, .5], fixed=True, color=[1, 0, 0]))

# ------ 创建下落球体 ------
# sphere() 创建一个球形 Body
#   [0, 0, 2.5]：球心初始位置，位于地面正上方（地面顶部 z=0.5，球底 z=1.5，间距 1.0）
#   1           ：球体半径为 1
#   color       ：绿色
O.bodies.append(sphere([0, 0, 2.5], 1, color=[0, 1, 0]))

# ============================================================
# 第三部分：设置时间步长并运行仿真
# ============================================================

# 导入 Qt 可视化模块并创建 3D 查看器
from yade import qt
v = qt.View()  # 打开图形窗口，显示颗粒运动动画

# 设置时间步长
# PWaveTimeStep() 根据最小颗粒尺寸和材料刚度计算 P 波（纵波）特征时间
# 乘以 0.002 作为安全系数，确保数值稳定性（越小越稳定，但计算越慢）
O.dt = 0.002 * PWaveTimeStep()

# 运行仿真（非阻塞模式，GUI 实时更新）
O.run(500000, False)

# 等待仿真完成，期间 GUI 会实时显示颗粒运动
import time
while O.running:
    time.sleep(0.1)

# ============================================================
# 第四部分：输出结果
# ============================================================

print("=" * 40)
print("仿真结束！")
print(f"球体最终位置：{O.bodies[1].state.pos}")
print(f"球体最终速度：{O.bodies[1].state.vel}")
print("=" * 40)

input("按回车键退出...")  # 保持窗口打开，方便观察最终状态
